-- | Map a repo name to one or more categories. Order of rules
-- matters: more-specific patterns must be matched before
-- more-general ones, and multi-category cases come first so the
-- primary classifier doesn\'t shadow them.
classify :: Text -> NonEmpty Category
classify name
    -- Multi-category: must come first so they win over the
    -- single-category fallthroughs further down.
    | name == "kritick-admin"                     =
        CatKritickProduct :| [CatKritickService]
    | name == "elasticsearch-kritick-schema"      =
        CatDatabaseInfra :| [CatKritickSchema]

    -- Specific kritick-* known names
    | name `elem` kritickCoordRepos               = single CatKritickCoordination
    | name `elem` kritickProductRepos             = single CatKritickProduct
    | name `elem` kritickSensitiveRepos           = single CatKritickProductSensitive

    -- Schema family + database infra (must come before generic
    -- prefix rules and the kritick-domain split)
    | name `elem` kritickSchemaRepos              = single CatKritickSchema
    | name `elem` databaseInfraRepos              = single CatDatabaseInfra

    -- Build / asset / package known names (specific before generic)
    | name `elem` buildToolingRepos               = single CatBuildTooling
    | name `elem` packageManagementRepos          = single CatPackageManagement
    | name `elem` assetsNonCodeRepos              = single CatAssetsNonCode

    -- Other ecosystem-relevant known-name lookups
    | name `elem` ngKritickAdjacentRepos          = single CatNgKritick
    | name `elem` experimentalRepos               = single CatExperimental

    -- Remaining kritick-* are services
    | "kritick-" `T.isPrefixOf` name              = single CatKritickService

    -- krikit-* (note the spelling difference: krikit, not kritick)
    | "krikit-" `T.isPrefixOf` name               = single CatKrikit

    -- PureScript kritick-domain split (data / react / other)
    | psKritickDataMatch name                     = single CatPureScriptKritickData
    | "purescript-kritick-react-" `T.isPrefixOf` name
                                                  = single CatPureScriptKritickReact
    | "purescript-kritick-" `T.isPrefixOf` name   = single CatPureScriptKritickOther

    -- PureScript React Native (more specific than purescript-react-).
    -- Match the bare `purescript-react-native` plus the dashed
    -- `purescript-react-native-*` family.
    | name == "purescript-react-native"           = single CatPureScriptReactNative
    | "purescript-react-native-" `T.isPrefixOf` name
                                                  = single CatPureScriptReactNative
    | "purescript-react-" `T.isPrefixOf` name     = single CatPureScriptReact
    -- Catches purescript-cloudinary-react / purescript-flexbox-react
    -- (React-related but missed by the prefix above).
    | "purescript-" `T.isPrefixOf` name && "-react" `T.isSuffixOf` name
                                                  = single CatPureScriptReact

    -- PureScript -free / -data
    | "purescript-" `T.isPrefixOf` name && "-free" `T.isSuffixOf` name
                                                  = single CatPureScriptFree
    | "purescript-" `T.isPrefixOf` name && "-data" `T.isSuffixOf` name
                                                  = single CatPureScriptData

    -- PureScript substructure for "other"
    | name `elem` purescriptCodecRepos            = single CatPureScriptCodecs
    | name `elem` purescriptStandardsRepos        = single CatPureScriptStandards
    | name `elem` purescriptDateTimeRepos         = single CatPureScriptDateTime
    | name `elem` purescriptGeographicRepos       = single CatPureScriptGeographic
    | name `elem` purescriptIdentifiersRepos      = single CatPureScriptIdentifiers
    | "purescript-" `T.isPrefixOf` name           = single CatPureScriptOther

    -- Scala
    | "scala-kritick-" `T.isPrefixOf` name        = single CatScalaKritick
    | "scala-" `T.isPrefixOf` name && "-free" `T.isSuffixOf` name
                                                  = single CatScalaFree
    | "scala-" `T.isPrefixOf` name && "-data" `T.isSuffixOf` name
                                                  = single CatScalaData
    | "scala-" `T.isPrefixOf` name                = single CatScalaOther

    -- Angular
    | "ng-kritick-" `T.isPrefixOf` name           = single CatNgKritick

    -- React
    | "react-" `T.isPrefixOf` name                = single CatReact

    -- Utility
    | "util-" `T.isPrefixOf` name                 = single CatUtil

    -- Templates
    | "template-" `T.isPrefixOf` name             = single CatTemplate

    -- Catch-all
    | otherwise                                   = single CatUnknown
  where
    single c = c :| []

    -- Suffix predicate for `purescript-kritick-...-data` that
    -- doesn\'t false-positive on `purescript-kritick-react-formfield`
    -- etc.: must start with the kritick prefix AND end with -data.
    psKritickDataMatch :: Text -> Bool
    psKritickDataMatch n =
        "purescript-kritick-" `T.isPrefixOf` n && "-data" `T.isSuffixOf` n

