-- | @krikit-regen-repo-inventory@: regenerate the categorized
-- repo inventory.
--
-- Inputs:
--
--   * @kritick-ecosystem\/docs\/ecosystem-roots.generated.md@
--     -- canonical exhaustive list of @kritick-*@ root repos.
--   * Filesystem walk of the workspace root (default
--     @~opsadmin\/Development\/@) -- catches every other prefix
--     (@krikit-*@, @purescript-*@, @scala-*@, @ng-kritick-*@, etc.)
--     plus surfaces uncategorized repos for follow-up.
--   * @kritick-ecosystem\/config\/ecosystem.json@ -- the @ignore@
--     and @skip_repo_prefixes@ filter rules.
--
-- Output:
-- @krikit-agent-fabric\/context\/repo-inventory.generated.md@.
--
-- Categorization is rule-based and *multi-category*: 'classify'
-- returns @'NonEmpty' 'Category'@ so a single repo can list under
-- more than one section. Examples:
--
--   * @elasticsearch-kritick-schema@ is database infrastructure
--     (Cloud-Formation + scripts) AND its mapping files are
--     autogenerated by @util-kritick-schema@. Lists in both.
--   * @kritick-admin@ is a customer-facing SPA AND the admin
--     backend service in one repo. Lists in both.
--
-- New categories require updating 'allCategories' + 'classify';
-- the compiler's exhaustiveness check on 'Category' enforces
-- consistency across heading, blurb, and dispatch.
