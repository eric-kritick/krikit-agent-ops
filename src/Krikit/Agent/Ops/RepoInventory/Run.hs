-- | Categories the inventory groups repos into. Order here defines
-- the section order in the rendered output.
--
-- @CatUnknown@ is the catch-all and always renders last; if any
-- repo lands there, that's a signal the taxonomy needs an update
-- and the verifier should flag it.
data Category
    -- Product (kritick-* customer-facing apps + supporting assets)
    = CatKritickProduct                -- ^ kritick-{admin, blog, customer, ...}
    | CatKritickProductSensitive       -- ^ supporting product assets, sensitive contents
    -- kritick-* services + coordination
    | CatKritickService                -- ^ kritick-{api, process, session, feature, ...}
    | CatKritickCoordination           -- ^ kritick-ecosystem
    -- krikit ops
    | CatKrikit                        -- ^ krikit-* (agent ops + fabric)
    -- Schema family
    | CatKritickSchema                 -- ^ util-kritick-schema + every *-kritick-*-schema output
    -- PureScript kritick-domain (split by sub-shape)
    | CatPureScriptKritickData         -- ^ purescript-kritick-*-data (domain ADTs)
    | CatPureScriptKritickReact        -- ^ purescript-kritick-react-* (kritick-themed React UI)
    | CatPureScriptKritickOther        -- ^ purescript-kritick-* (programs, dateutil, email-templates...)
    -- PureScript general
    | CatPureScriptFree                -- ^ purescript-*-free
    | CatPureScriptData                -- ^ purescript-*-data
    | CatPureScriptReact               -- ^ purescript-react-* (web)
    | CatPureScriptReactNative         -- ^ purescript-react-native-*
    | CatPureScriptCodecs              -- ^ encoding / serialization libraries
    | CatPureScriptStandards           -- ^ ISO standards (3166, 4217, 639, 8601)
    | CatPureScriptDateTime            -- ^ date / time helpers
    | CatPureScriptGeographic          -- ^ geographic data types
    | CatPureScriptIdentifiers         -- ^ id-shaped value types
    | CatPureScriptOther               -- ^ remaining purescript-* misc
    -- Scala
    | CatScalaKritick                  -- ^ scala-kritick-* (non-schema)
    | CatScalaFree                     -- ^ scala-*-free (non-kritick)
    | CatScalaData                     -- ^ scala-*-data (non-kritick)
